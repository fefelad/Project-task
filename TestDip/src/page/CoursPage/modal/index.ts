export const sortCourseCards = (cards: CourseCard[]): CourseCard[] =>
  [...cards].sort((a, b) => {
    const directionCompare =
      DESIGN_DIRECTIONS.indexOf(a.directions[0]) -
      DESIGN_DIRECTIONS.indexOf(b.directions[0])

    if (directionCompare !== 0) {
      return directionCompare
    }

    return a.stage - b.stage
  })

export interface CourseDirectionGroup {
  direction: DesignDirection
  cards: CourseCard[]
}

export const getCourseGroups = (): CourseDirectionGroup[] =>
  DESIGN_DIRECTIONS.map(direction => ({
    direction,
    cards: sortCourseCards(
      courseCards.filter(card => card.directions[0] === direction)
    ),
  }))

